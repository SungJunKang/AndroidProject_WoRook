package sungjunkang.worook;

public class WordBook_Item {
    private String wordbook_name;
    private String wordbook_date;
    private String wordbook_token;

    public String getWordbook_date() {
        return wordbook_date;
    }

    public String getWordbook_name() {
        return wordbook_name;
    }

    public String getWordbook_token() {
        return wordbook_token;
    }

    public void setWordbook_date(String wordbook_date) {
        this.wordbook_date = wordbook_date;
    }

    public void setWordbook_name(String wordbook_name) {
        this.wordbook_name = wordbook_name;
    }

    public void setWordbook_token(String wordbook_token) {
        this.wordbook_token = wordbook_token;
    }
}
